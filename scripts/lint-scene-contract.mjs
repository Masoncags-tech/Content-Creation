import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const files = process.argv.slice(2);

if (files.length === 0) {
  files.push("templates/scene-design-prompt.md");
}

const requiredPhrases = [
  "## Script Contract",
  "## Scene Contract",
  "## Motion Contract",
  "## Prop Contract",
  "## UI/Text Contract",
  "## Audio Contract",
  "## Negative Prompt Ledger",
  "## QA Forecast",
  "- First visible event:",
  "- Spoken trigger:",
  "- Location:",
  "- Camera source:",
  "- Starting frame:",
  "Any readable text generated by model?",
  "Proof insert source:",
];

const riskyPositivePatterns = [
  /Any readable text generated by model\?\s*`?yes`?/i,
  /generated\s+(calendar|dashboard|app|dm|receipt)\s+text/i,
  /fake\s+(client|customer|metric|revenue|dashboard)/i,
];

for (const file of files) {
  const absolute = path.resolve(root, file);
  assert.ok(fs.existsSync(absolute), `Missing scene contract: ${file}`);
  const body = fs.readFileSync(absolute, "utf8");

  for (const phrase of requiredPhrases) {
    assert.ok(body.includes(phrase), `${file} missing required scene-contract phrase: ${phrase}`);
  }

  for (const pattern of riskyPositivePatterns) {
    assert.ok(!pattern.test(body), `${file} contains risky generated-proof language: ${pattern}`);
  }
}

console.log(`Scene contract lint passed for ${files.length} file(s).`);

